import json
from typing import Dict

CUSTOM_CONFIG_FILENAME = "custom.json"


def read_custom_config() -> Dict:
    with open(CUSTOM_CONFIG_FILENAME, "r", encoding="utf-8") as fh:
        return json.load(fh)
